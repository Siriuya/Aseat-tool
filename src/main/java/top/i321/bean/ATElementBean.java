package top.i321.bean;

public class ATElementBean {
    private String elementId;
    private String elementName;
    private String variableName;
    private String iframeXPath;
    private String name;
    private String id;
    private String xpath;
    private String className;
    private String cssSelector;
    private String tagName;
    private String linkText;
    private String partialLinkText;
    private String options;

    public String getElementName() {
        return elementName;
    }

    public void setElementName(String elementName) {
        this.elementName = elementName;
    }

    public String getVariableName() {
        return variableName;
    }

    public void setVariableName(String variableName) {
        this.variableName = variableName;
    }

    public String getIframeXPath() {
        return iframeXPath;
    }

    public void setIframeXPath(String iframeXPath) {
        this.iframeXPath = iframeXPath;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getXpath() {
        return xpath;
    }

    public void setXpath(String xpath) {
        this.xpath = xpath;
    }

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public String getCssSelector() {
        return cssSelector;
    }

    public void setCssSelector(String cssSelector) {
        this.cssSelector = cssSelector;
    }

    public String getTagName() {
        return tagName;
    }

    public void setTagName(String tagName) {
        this.tagName = tagName;
    }

    public String getLinkText() {
        return linkText;
    }

    public void setLinkText(String linkText) {
        this.linkText = linkText;
    }

    public String getPartialLinkText() {
        return partialLinkText;
    }

    public void setPartialLinkText(String partialLinkText) {
        this.partialLinkText = partialLinkText;
    }

    public String getOptions() {
        return options;
    }

    public void setOptions(String options) {
        this.options = options;
    }


    public String getElementId() {
        return elementId;
    }

    public void setElementId(String elementId) {
        this.elementId = elementId;
    }


}
